# here is 1 dim list
list_1d = [1, 2, 3, 4]
# here 2 dim list
list_2d = [[1, 2], [3, 4], [5, 6], [7, 8]]
# and 3d list
list_3d = [
    [[1, 2, 3]], [[4, 5, 6]], [[7, 8, 9]], [[1, 2, 3]]
    ]


# lets see what we can print
print(list_1d)
print(list_2d)
print(list_3d)

print(list_1d[1])  # print 1st element
print(list_2d[1][1])  # 2nd element with second item
print(list_3d[3][0][2])  # 4th element 3rd item
